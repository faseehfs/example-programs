// JavaScript is a dynamically typed language.
// You don't want to specify the type while declaring a variable.

// Primitive Data Types
let str = "Hello, World!";          // String
let num = 42;                       // Number (includes both int and float)
let bigInt = 123456789123456n;      // BigInt (Suffix with 'n')
let bool = true;                    // Boolean
let undef;                          // Undefined
let emptyValue = null;              // Null

// Non-Primitive Data Types
let obj = {name: "Alice", age: 25}; // Object
let arr = [1, 2, 3, 4];             // Array (mutable, dynamic size)
let date = new Date();              // Date

// Special Cases
let notANumber = "abc" / 3;         // NaN
let infinityValue = 1 / 0;          // Infinity
